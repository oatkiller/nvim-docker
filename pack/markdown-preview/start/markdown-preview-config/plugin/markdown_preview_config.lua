-- markdown_preview_config.lua
-- Configuration for iamcco/markdown-preview.nvim

-- Do not automatically open the preview when editing markdown
vim.g.mkdp_auto_start = 0

-- Close the preview window when leaving the buffer
vim.g.mkdp_auto_close = 1

-- Echo the preview page URL in command line for easy copy-paste
